<script setup lang="ts">
import { ApplicationType } from '@shared-utils/types/defaultTypes'
import SaveButton from './SaveButton.vue'
import { reactive } from 'vue'
import { useMutation } from '@tanstack/vue-query'
import { usePermitsStore } from '@core-admin/stores/permitsStore'

const permitStore = usePermitsStore()
const emit = defineEmits(['on-save'])
const state = reactive({
  ssn: '',
  valid: false,
})

const {
  isError,
  isLoading,
  mutate: ssnMutation,
} = useMutation(
  ['ssnMutation'],
  () => permitStore.getPermitSsn(permitStore.getPermitDetail.userId),
  {
    onSuccess: res => {
      state.ssn = res
    },
  }
)

function getSSN() {
  ssnMutation()
}

function hideSsn() {
  state.ssn = ''
}

function handleSave() {
  emit('on-save', 'Application Info')
}

function onApproveNameChange() {}

function onCheckNameChangeDocument() {}
</script>
